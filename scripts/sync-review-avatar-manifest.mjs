import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const avatarDir = path.join(__dirname, "../public/images/review-profile");
const manifestPath = path.join(__dirname, "../src/lib/review-avatar-manifest.ts");

const AVATAR_PATTERN = /^avatar-\d+\.webp$/i;

function main() {
  if (!fs.existsSync(avatarDir)) {
    fs.mkdirSync(avatarDir, { recursive: true });
  }

  const files = fs
    .readdirSync(avatarDir)
    .filter((name) => AVATAR_PATTERN.test(name))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

  const contents = `/** Auto-generated by scripts/sync-review-avatar-manifest.mjs — do not edit manually */
export const REVIEW_AVATAR_FILES = ${JSON.stringify(files, null, 2)} as const;

export type ReviewAvatarFile = (typeof REVIEW_AVATAR_FILES)[number];
`;

  fs.writeFileSync(manifestPath, contents, "utf8");
  console.log(`Synced ${files.length} review avatar(s) to src/lib/review-avatar-manifest.ts`);
}

main();
